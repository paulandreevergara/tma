<h4>ADMISSION PROCEDURES FOR HIGHSCHOOL LEVEL</h4>
1.	Applicants are admitted after they have passed the admission test on the following subjects: English and Mathematics<br>
2.	Transferees are admitted only if there are slots and if they have passed the admission test on the following subjects: English and<br> Mathematics. (An examination fee shall be charged)<br>
Who should take the examination:<br>
&nbsp &nbsp &nbsp &nbsp *	Elementary School Graduates<br>
&nbsp &nbsp &nbsp &nbsp *	Grade 7 Students who failed in 4 or more subjects<br>
&nbsp &nbsp &nbsp &nbsp *	Grade 7 Students who dropped out during the school year<br>
