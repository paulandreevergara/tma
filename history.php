			<div class="cd-timeline-content">
				<p>In June 1923, a third year class was added, and in June 1924, the complete high school course was given. On March 25, 1925 it received government recognition. </p>
				
				<span class="cd-date"></span>
			</div> <!-- cd-timeline-content -->
		</div> <!-- cd-timeline-block -->

