			<div class="cd-timeline-content">
				<h2>Humble Beginnings of the Academy..</h2>
				<p>The Mabini Academy of Lipa City, Batangas, Philippines was founded as a non- stock, non-profit corporation on  June 10, 1922 by Professor Randall A.  Rowley, Dean Francisco Binetez, Mrs. Paz M. Binetez, Dr. Jose M. Katigbak, Mrs. Tarcilla M. Katigbak and Miss Emilia Malabanan. It opened June 12, 1922 with an enrollment of more than one hundred students, two first year classes and one second year class.</p>
				
				<span class="cd-date">  <br><br><img class="history-img" src="images/history.jpg"></span>
