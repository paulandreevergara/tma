				<p>
	Before the war, the school occupied a large three-storey building with a library and laboratories.  That building was burned, the apparatus and library were looted and burned during the war. By March 1947 the library had been replenish and the laboratories equipped. In the same year the school transferred to its present site.
</p>
				
				<span class="cd-date"></span>
			</div> <!-- cd-timeline-content -->
