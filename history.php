			<div class="cd-timeline-content">
				<h2>Rebuilding of The Mabini Academy..</h2>
				<p>The administration building was the first to be demolished and replaced by a modern concrete edifice. In 1964, the two wings of this building, housing eight classrooms, were constructed. This was followed by remodeling of the grandstand in 1965, the construction of the first wing of the two-storey Science building in 1966, the construction of a 3-room Vocational Building in 1967, and the construction of a storey Science Building was completed.</p>
				
				<span class="cd-date"></span>
