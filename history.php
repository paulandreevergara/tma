			<div class="cd-timeline-content">
				<h2>Relocation of The Mabini Academy..</h2>
				<p>The site has been enlarged by buying adjoining lots. Its present area is 25, 610 square meters – one of the largest school sites among private schools in Batangas. On this site, the Academy has seven buildings, a basketball court, and an open stage. In 1948 a Junior College with liberal arts and normal courses was added together with a complete elementary department. The College Department however, was short-lived, closing in 1961.</p>
				
				<span class="cd-date"></span>
