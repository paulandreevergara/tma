				<h2>During the Second World War..</h2>
				<p>It continued functioning as a secondary school, giving the complete general course (day) until December 9, 1941, when all schools closed because of the Second World War. It remained closed during the Japanese occupation. It reopened in June in June 1945 and has been functioning ever since.
</p>
				
				<span class="cd-date"></span>
			</div> <!-- cd-timeline-content -->
